test("integration: Error context propagates through plan graph creation", () => {
  const principal = createPrincipalRef({
    principalId: "user-1",
    tenantId: "tenant-1",
    roles: ["operator"],
  });

  const riskPreview = {
    riskClass: "high" as const,
    reasons: ["External API call"],
  };

  const confirmedSpec = createConfirmedTaskSpec({
    taskDraftId: "draft-error-1",
    tenantId: "tenant-1",
    principal,
    goal: "Call external API",
    inputs: {},
    constraintPackRef: "cp-error-1",
    riskClass: "high",
    idempotencyKey: "idem-error-1",
    traceId: "trace-error-1",
  });

  const envelope = createRequestEnvelopeFromConfirmedTask({
    confirmedTaskSpec: confirmedSpec,
    budgetIntent: { amount: 100, currency: "USD", resourceKinds: ["token", "api"] },
  });

  const run = createHarnessRun({
    tenantId: envelope.tenantId,
    confirmedTaskSpecId: envelope.confirmedTaskSpecId,
    requestEnvelopeId: envelope.requestId,
    requestHash: envelope.requestHash,
    constraintPackRef: envelope.constraintPackRef,
    versionLockId: "vl-error-1",
    budgetLedgerId: "bledger-error-1",
  });

  const graph = {
    graphId: "graph-error-1",
    nodes: [{
      nodeId: "node-error-1",
      nodeType: "tool" as const,
      inputRefs: [],
      outputSchemaRef: "schema://output",
      riskClass: "high" as const,
      budgetIntent: { amount: 50, currency: "USD", resourceKinds: ["api"] },
      sideEffectProfile: { mayCommitExternalEffect: true, reversible: false },
      retryPolicyRef: "retry://default",
      timeoutMs: 30000,
    }],
    edges: [],
    entryNodeIds: ["node-error-1"],
    terminalNodeIds: ["node-error-1"],
    joinStrategy: "all" as const,
    graphHash: "hash-error-1",
  };

  const bundle = createPlanGraphBundle({
    harnessRunId: run.harnessRunId,
    graph,
    schedulerPolicy: { policyId: "scheduler-error", strategy: "priority_then_fifo" },
    budgetPlanRef: "budget-plan-error",
    riskProfile: riskPreview,
  });

  assert.ok(bundle.planGraphBundleId.startsWith("pgb_"));
  assert.equal(bundle.graph.nodes[0].riskClass, "high");
});

test("integration: Multiple error types can be distinguished in batch processing", () => {
  const errors = [
    new ValidationError("E1", "Validation 1"),
    new PolicyDeniedError("E2", "Policy 1"),
    new AuthError("E3", "Auth 1"),
    new ProviderError("E4", "Provider 1"),
  ];

  const categories = errors.map(e => e.category);
  const retryable = errors.map(e => e.retryable);

  assert.deepEqual(categories, ["validation", "policy", "auth", "provider"]);
  assert.deepEqual(retryable, [false, false, false, true]);  // ProviderError is retryable
});

