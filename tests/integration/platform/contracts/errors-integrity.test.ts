test("integration: AppError.wrap returns same instance for AppError (no double-wrapping)", () => {
  const original = new ProviderError("ORIGINAL", "Original provider error");

  // Wrap the error with additional context
  const wrapped = AppError.wrap(original, "WRAPPED", "Wrapped with context", {
    taskId: "task-recovery-1",
  });

  // AppError.wrap returns same instance when wrapping an AppError (no double-wrapping)
  assert.strictEqual(wrapped, original);
  assert.equal(wrapped.code, "ORIGINAL");  // Original code preserved
  assert.equal(wrapped.taskId, null);  // Original taskId preserved (not overwritten)
});

test("integration: AppError.wrap creates new wrapper for non-AppError errors", () => {
  const original = new Error("Original error from external module");

  // Wrap a non-AppError
  const wrapped = AppError.wrap(original, "WRAPPED", "Wrapped with context", {
    taskId: "task-recovery-1",
  });

  // Should create new AppError instance
  assert.notStrictEqual(wrapped, original);
  assert.equal(wrapped.code, "WRAPPED");
  assert.equal(wrapped.taskId, "task-recovery-1");
  assert.ok(wrapped.internalDetails !== null);
  assert.ok("originalError" in wrapped.internalDetails);
});

