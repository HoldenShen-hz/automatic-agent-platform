test("integration: ValidationError thrown during budget operation is properly typed", () => {
  const ledger = createBudgetLedger({
    tenantId: "tenant-1",
    harnessRunId: "hrun-val-1",
    currency: "USD",
    hardCap: 100,
  });

  // Reserve some budget
  const result1 = reserveBudgetHardCap({
    ledger,
    amount: 30,
    resourceKind: "token",
    expiresAt: "2026-04-30T00:00:00.000Z",
    expectedVersion: ledger.version,
  });

  assert.equal(result1.ledger.reservedAmount, 30);

  // Try to exceed cap
  let caughtError: unknown = null;
  try {
    reserveBudgetHardCap({
      ledger: result1.ledger,
      amount: 100,  // Exceeds remaining 70
      resourceKind: "token",
      expiresAt: "2026-04-30T00:00:00.000Z",
      expectedVersion: result1.ledger.version,
    });
  } catch (error) {
    caughtError = error;
  }

  // The error should be an AppError subclass
  assert.ok(caughtError instanceof AppError);
  assert.equal(caughtError.category, "validation");
  assert.equal(caughtError.code, "budget_reservation.hard_cap_exceeded");
});

